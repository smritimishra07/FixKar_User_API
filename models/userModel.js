const mongoose = require('mongoose');

const userSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  phone: {
    type: String,
    required: true,
    unique: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  location: {
    type: String,
    required: true,
  },
//   profilePhoto: {
//     type: String, 
//   },
}, { timestamps: true });

const User = mongoose.model('User', userSchema);

module.exports = User;
